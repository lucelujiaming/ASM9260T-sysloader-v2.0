int draw_asc(long row,long col,long eng){
    int en_count,i,j;
    long rows,cols;
    unsigned char p;
    en_count = (eng << 4);
    if (row >= BYTES_ROW) {
        printf("too big row\n");
        return -1;
    }
    if (col >= BYTES_COL) {
        printf("too big col\n");
        return -1;
    }
    //printf("row:%d,col:%d,eng:%d\n",row,col,eng);

    for (i=0;i<8;i++) {
        rows = ENG_ROW*row+i;
        cols = ENG_COL*col;
        //printf("rows:%d,cols:%d\n",rows,cols);
        p=0x01;
        for (j=0;j<8;j++) {
            if (p&FontEng[en_count]) {
                draw_p(rows,cols);
            }      
            cols++;
            p =p<<1;
        }
        en_count++;
        p = 0x01;
        for (j=0;j<8;j++) {
            if (p&FontEng[en_count]) {
                draw_p(rows,cols);
            }            
            cols++;
            p =p<<1;
        }
        en_count++;
    }
    return 0;
}


int lcd_printf(long row,long col,char *idata){
    int i,j,icount,retval;
    icount = 0;
    j = col;
    i = row;
    while(idata[icount]!='\0'){
        //printf("idata:%c icount:%d i:%d j:%d\n",idata[icount],icount,i,j);
        if (i == BYTES_ROW) {
            i = 0;
            j++;
        }
        if (j == BYTES_COL) {
            j = 0;
        }
        retval = draw_asc(i++,j,(long)idata[icount]);
        if (retval != 0) {
            printf("lcd_printf error\n");
        }
        icount++;
    }
    return icount;
}

 

